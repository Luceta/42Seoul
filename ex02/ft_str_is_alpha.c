int	ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != 0)
	{
		if (str[i] > 122 || (90 < str[i] && str[i] < 97) || 65 > str[i])
			return (0);
		i++;
	}
	return (1);
}
