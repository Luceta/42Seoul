#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	strexp;

	strexp = 0;
	while (str[strexp])
	{
		ft_putchar(str[strexp]);
		strexp++;
	}
}
