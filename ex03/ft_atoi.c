int	ft_atoi(char *str)
{
	int i;
	int j;
	int m;

	i = 0;
	m = 1;
	j = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == ' ') && str[i] != 0)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = -m;
		i++;
	}
	if (str[i] == 0)
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = (j * 10) + (str[i] - '0');
		i++;
	}
	return (j * m);
}
